<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class HCuentasEfectivo extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\CuentasEfectivo, 'name' );  // generated by relation command - CuentasEfectivo,HCuentasEfectivo
    } 

	//Mass Assignment
	protected $fillable = ['cuentas_efectivo_id','saldo_inicial','saldo_actualizado','fecha_saldo_inicial','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end


    protected $dates = ['deleted_at'];

	// generated by relation command - CuentasEfectivo,HCuentasEfectivo - start
	public function cuentasEfectivo() {
		return $this->belongsTo('App\CuentasEfectivo');
	}// end
}