    Route::get('zip/imagenes', function () {
            $files = glob(public_path('imagenes'));
            if(file_exists(public_path().'\\archivos_imagenes.zip')){
                unlink(public_path().'\\archivos_imagenes.zip');
            }
            Zipper::make(public_path().'\\archivos_imagenes.zip')->add($files);
            while(!file_exists(public_path().'\\archivos_imagenes.zip')){
                sleep(3);
            }
            return response()->download(public_path().'\\archivos_imagenes.zip');
    })->name('zip.imagenes');
