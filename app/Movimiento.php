<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class Movimiento extends Model
{
	use RelationManagerTrait, GetAllDataTrait;
	use SoftDeletes;

	public function __construct(array $attributes = array())
	{
		parent::__construct($attributes);
		$this->addRelationApp(new \App\Plantel, 'razon');  // generated by relation command - Plantel,Movimiento
		$this->addRelationApp(new \App\Articulo, 'name');  // generated by relation command - Articulo,Movimiento
		$this->addRelationApp(new \App\EntradaSalida, 'name');  // generated by relation command - EntradaSalida,Movimiento
		$this->addRelationApp(new \App\UbicacionArt, 'Ubicacion');  // generated by relation command - EntradaSalida,Movimiento
	}

	//Mass Assignment
	protected $fillable = [
		'plantel_id', 'articulo_id', 'unidad', 'cantidad', 'fecha', 'entrada_salida_id', 'usu_alta_id', 'usu_mod_id',
		'ubicacion_art_id', 'empleado_id', 'marca', 'modelo', 'no_serie', 'caducidad', 'observaciones', 'costo'
	];

	public function usu_alta()
	{
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	} // end

	public function usu_mod()
	{
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	} // end


	protected $dates = ['deleted_at'];

	// generated by relation command - Plantel,Movimiento - start
	public function plantel()
	{
		return $this->belongsTo('App\Plantel');
	} // end

	// generated by relation command - Articulo,Movimiento - start
	public function articulo()
	{
		return $this->belongsTo('App\Articulo');
	} // end

	// generated by relation command - EntradaSalida,Movimiento - start
	public function entradaSalida()
	{
		return $this->belongsTo('App\EntradaSalida');
	} // end

	// generated by relation command - EntradaSalida,Movimiento - start
	public function ubicacionArt()
	{
		return $this->belongsTo('App\UbicacionArt');
	} // end
}
