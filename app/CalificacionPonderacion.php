<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class CalificacionPonderacion extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\Calificacion, 'calificacion' );  // generated by relation command - Calificacion,CalificacionPonderacion
    } 

	//Mass Assignment
	protected $fillable = ['ponderacion','calificacion_id','carga_ponderacion_id','calificacion_parcial','calificacion_parcial_calculada','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end


    protected $dates = ['deleted_at'];

	// generated by relation command - Calificacion,CalificacionPonderacion - start
	public function calificacion() {
		return $this->belongsTo('App\Calificacion');
	}// end
}