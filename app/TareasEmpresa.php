<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class TareasEmpresa extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\Empresa, 'razon_social' );  // generated by relation command - Empresa,TareasEmpresa
		$this->addRelationApp( new \App\Empleado, 'nombre' );  // generated by relation command - Empleado,TareasEmpresa
		$this->addRelationApp( new \App\Tarea, 'name' );  // generated by relation command - Tarea,TareasEmpresa
		$this->addRelationApp( new \App\Asunto, 'name' );  // generated by relation command - Asunto,TareasEmpresa
		$this->addRelationApp( new \App\StTarea, 'name' );  // generated by relation command - StTarea,TareasEmpresa
    } 

	//Mass Assignment
	protected $fillable = ['empresa_id','empleado_id','tarea_id','asunto_id','detalle','st_tarea_id','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end


    protected $dates = ['deleted_at'];

	// generated by relation command - Empresa,TareasEmpresa - start
	public function empresa() {
		return $this->belongsTo('App\Empresa');
	}// end

	// generated by relation command - Empleado,TareasEmpresa - start
	public function empleado() {
		return $this->belongsTo('App\Empleado');
	}// end

	// generated by relation command - Tarea,TareasEmpresa - start
	public function tarea() {
		return $this->belongsTo('App\Tarea');
	}// end

	// generated by relation command - Asunto,TareasEmpresa - start
	public function asunto() {
		return $this->belongsTo('App\Asunto');
	}// end

	// generated by relation command - StTarea,TareasEmpresa - start
	public function stTarea() {
		return $this->belongsTo('App\StTarea');
	}// end
}