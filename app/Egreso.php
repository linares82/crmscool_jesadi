<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class Egreso extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\Plantel, 'razon' );  // generated by relation command - Plantel,Egreso
		$this->addRelationApp( new \App\EgresosConcepto, 'name' );  // generated by relation command - EgresosConcepto,Egreso
		$this->addRelationApp( new \App\FormaPago, 'name' );  // generated by relation command - FormaPago,Egreso
		$this->addRelationApp( new \App\CuentasEfectivo, 'name' );  // generated by relation command - CuentasEfectivo,Egreso
		$this->addRelationApp( new \App\Empleado, 'nombre' );  // generated by relation command - Empleado,Egreso
    } 

	//Mass Assignment
	protected $fillable = ['plantel_id','fecha','egresos_concepto_id','detalle','archivo','forma_pago_id','cuentas_efectivo_id','monto','empleado_id','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end


    protected $dates = ['deleted_at'];

	// generated by relation command - Plantel,Egreso - start
	public function plantel() {
		return $this->belongsTo('App\Plantel');
	}// end

	// generated by relation command - EgresosConcepto,Egreso - start
	public function egresosConcepto() {
		return $this->belongsTo('App\EgresosConcepto');
	}// end

	// generated by relation command - FormaPago,Egreso - start
	public function formaPago() {
		return $this->belongsTo('App\FormaPago');
	}// end

	// generated by relation command - CuentasEfectivo,Egreso - start
	public function cuentasEfectivo() {
		return $this->belongsTo('App\CuentasEfectivo');
	}// end

	// generated by relation command - Empleado,Egreso - start
	public function empleado() {
		return $this->belongsTo('App\Empleado');
	}// end
}