<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class AsistenciaR extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\Cliente, 'nombre' );  // generated by relation command - Cliente,AsistenciaR
		$this->addRelationApp( new \App\EstAsistencium, 'name' );  // generated by relation command - EstAsistencium,AsistenciaR
    } 

	//Mass Assignment
	protected $fillable = ['asignacion_academica_id','fecha','cliente_id','est_asistencia_id','usu_alta_id','usu_mod_id'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end
        
    protected $dates = ['deleted_at'];

	// generated by relation command - Cliente,AsistenciaR - start
	public function cliente() {
		return $this->belongsTo('App\Cliente');
	}// end

	// generated by relation command - EstAsistencium,AsistenciaR - start
	public function estAsistencium() {
		return $this->belongsTo('App\EstAsistencium','est_asistencia_id','id');
	}// end
}