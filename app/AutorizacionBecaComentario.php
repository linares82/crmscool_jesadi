<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use App\Traits\GetAllDataTrait;
use App\Traits\RelationManagerTrait;
use Illuminate\Database\Eloquent\SoftDeletes;

class AutorizacionBecaComentario extends Model
{
    use RelationManagerTrait,GetAllDataTrait;
    use SoftDeletes;

    public function __construct(array $attributes = array())
    {
        parent::__construct($attributes);
		$this->addRelationApp( new \App\AutorizacionBeca, 'solicitud' );  // generated by relation command - AutorizacionBeca,AutorizacionBecaComentario
		$this->addRelationApp( new \App\StBeca, 'name' );  // generated by relation command - StBeca,AutorizacionBecaComentario
    } 

	//Mass Assignment
	protected $fillable = ['autorizacion_beca_id','comentario','monto_inscripcion','monto_mensualidad','st_beca_id','usu_alta_id','usu_mod_id','bnd_visto'];

	public function usu_alta() {
		return $this->hasOne('App\User', 'id', 'usu_alta_id');
	}// end

	public function usu_mod() {
		return $this->hasOne('App\User', 'id', 'usu_mod_id');
	}// end


    protected $dates = ['deleted_at'];

	// generated by relation command - AutorizacionBeca,AutorizacionBecaComentario - start
	public function autorizacionBeca() {
		return $this->belongsTo('App\AutorizacionBeca');
	}// end

	// generated by relation command - StBeca,AutorizacionBecaComentario - start
	public function stBeca() {
		return $this->belongsTo('App\StBeca');
	}// end
}